// -----------------------------------------------------------------------------
// Launch the Warehouse page WITHOUT a WinCC OA backend — interactive.
//
//   node tools/warehouse-standalone.mjs [--open] [--no-open] [--port N] [--keep]
//
// This is the *interactive* twin of tools/screenshot-warehouse-demo.mjs: instead
// of driving Playwright and quitting, it starts the Vite dev server, writes a
// standalone harness page and leaves everything running so you can USE the
// Warehouse UI in your browser (all five tabs, 2D/3D plan, dialogs, inventory…)
// with the built-in in-memory demo dataset. Ctrl+C stops the server.
//
// How the "no WinCC OA" mode works (nothing to configure):
//   • Every warehouse store transparently falls back to an in-memory demo set
//     when the PARA backend is unreachable (see libs/wui-warehouse/src/warehouse/
//     data/stores.ts + stock-store.ts). Writes stay in memory for the session.
//   • We start the dev server with BASE_URL pointing at a DEAD port so every
//     /api|/WebUI_* call fails fast → the offline fallback engages.
//   • The normal dashboard shell needs a login (against BASE_URL) that cannot
//     succeed here, so instead of the shell we mount <wui-warehouse> directly
//     through a minimal harness HTML (iX theme + DI services, no shell/router/
//     login). A tiny router shim mirrors the master/detail navigation into the
//     component's `warehouseid` attribute and the URL hash.
//
// Prerequisite: a wired runtime workspace (apps/dashboard-wc/ must exist). If it
// is missing this script prints the two setup commands and exits. See
// DEVELOPMENT.md §1 "Prepare a dev workspace".
// -----------------------------------------------------------------------------
import { spawn } from 'node:child_process';
import { existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const hasFlag = (name) => process.argv.includes(`--${name}`);
const flagValue = (name, fallback) => {
  const index = process.argv.indexOf(`--${name}`);
  return index !== -1 && process.argv[index + 1] ? process.argv[index + 1] : fallback;
};

const PORT = Number(flagValue('port', '4300'));
const DEV_URL = `http://127.0.0.1:${PORT}`;
// A dead proxy target: every /api|/WebUI_* call fails fast → offline demo mode.
const DEAD_BASE_URL = 'https://127.0.0.1:59999';
const APP_DIR = path.join(repoRoot, 'apps/dashboard-wc');
const HARNESS = path.join(APP_DIR, 'warehouse-standalone.html');
const BOOT = path.join(APP_DIR, 'warehouse-standalone-boot.js');
const HARNESS_URL = `${DEV_URL}/warehouse-standalone.html`;
// --open by default; --no-open disables it.
const OPEN = hasFlag('open') || !hasFlag('no-open');

// The bootstrap is an EXTERNAL module (not an inline <script type="module">):
// Vite extracts inline module scripts into "html-proxy" virtual modules, which
// intermittently fail on a hand-written HTML served this way ("No matching HTML
// proxy module found …?html-proxy&index=0.js"). An external module is served and
// transformed normally, sidestepping that path entirely.
const HARNESS_HTML = `<!doctype html>
<!-- Generated by tools/warehouse-standalone.mjs — safe to delete, regenerated on launch. -->
<html lang="fr" data-ix-theme="classic" data-ix-color-schema="dark">
  <head>
    <meta charset="utf-8" />
    <title>Warehouse — mode autonome (sans WinCC OA)</title>
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <meta name="ix:legacy-icons" content="false" />
    <link rel="stylesheet" href="/src/styles.scss" />
    <style>
      html, body { height: 100%; margin: 0; }
      wui-warehouse { display: block; height: 100%; }
    </style>
  </head>
  <body>
    <wui-warehouse></wui-warehouse>
    <script type="module" src="/warehouse-standalone-boot.js"></script>
  </body>
</html>
`;

const BOOT_JS = `// Generated by tools/warehouse-standalone.mjs — safe to delete, regenerated on launch.
import 'reflect-metadata'; // tsyringe — must precede the services
import '@wincc-oa/wui-ix-wrappers/wui-ix-loader/wui-ix-loader.js'; // iX theme + icons
import '@wincc-oa/wui-base/wui-register-services.js'; // DI registrations

// Load the page like the shell router does: a runtime dynamic import of the
// '?import' URL, which the dev-server bypass serves from the TypeScript source
// (see createProxyConfig in vite.shared.ts). Static imports won't do: vite's
// import-analysis fails on the virtual /data/... path.
await import(/* @vite-ignore */ '/data/dashboard-wc/pages/warehouse.js' + '?import');

const el = document.querySelector('wui-warehouse');
const idFromHash = () => {
  const match = /^#\\/warehouse\\/([^/]+)$/.exec(globalThis.location.hash || '');
  return match ? decodeURIComponent(match[1]) : '';
};
const applyId = (id) => {
  if (id) el.setAttribute('warehouseid', id);
  else el.removeAttribute('warehouseid');
};
// Master/detail: the page navigates by dispatching RouterEvent('navigateTo').
// Mirror /warehouse/:id into the URL hash (so refresh/back keep context) and
// into the component attribute, like the shell router would.
globalThis.addEventListener('navigateTo', (e) => {
  const match = /^\\/warehouse\\/([^/]+)$/.exec(e.detail?.pathname ?? '');
  globalThis.location.hash = match ? '/warehouse/' + match[1] : '/warehouse';
});
globalThis.addEventListener('hashchange', () => applyId(idFromHash()));
applyId(idFromHash()); // honor a deep link on first load
`;

function preflight() {
  if (existsSync(APP_DIR)) return;
  console.error(
    [
      '',
      '✗ Workspace non câblé : apps/dashboard-wc/ est absent.',
      '',
      '  Ce repo est la SOURCE des pages, pas un workspace exécutable. Le serveur',
      '  de dev vit dans un workspace @wincc-oa/webui-runtime. Une fois (voir',
      '  DEVELOPMENT.md §1), dans ce dossier :',
      '',
      '    npm install @wincc-oa/webui-runtime',
      '    npx webui-runtime-init',
      '    npm install --save-dev --no-audit --no-fund',
      '    npm run init:oa-data',
      '    node tools/wire-workspace.mjs',
      '    node tools/install-page-dependencies.mjs',
      '',
      '  Puis relance :  node tools/warehouse-standalone.mjs',
      ''
    ].join('\n')
  );
  process.exit(1);
}

async function reachable(url) {
  try {
    const response = await fetch(url, { signal: AbortSignal.timeout(2000) });
    return response.ok;
  } catch {
    return false;
  }
}

function openBrowser(url) {
  const command =
    process.platform === 'win32' ? ['cmd', ['/c', 'start', '', url]] : process.platform === 'darwin' ? ['open', [url]] : ['xdg-open', [url]];
  try {
    spawn(command[0], command[1], { stdio: 'ignore', detached: true }).unref();
  } catch {
    /* the printed URL is the fallback */
  }
}

async function main() {
  preflight();
  mkdirSync(APP_DIR, { recursive: true });
  writeFileSync(HARNESS, HARNESS_HTML);
  writeFileSync(BOOT, BOOT_JS);

  console.log('· Warehouse — mode autonome (sans WinCC OA)');
  console.log(`· démarrage du serveur de dev (BASE_URL=${DEAD_BASE_URL} — mort volontairement)`);

  const child = spawn('npm', ['start'], {
    cwd: repoRoot,
    env: { ...process.env, BASE_URL: DEAD_BASE_URL, PORT: String(PORT) },
    stdio: 'inherit',
    shell: process.platform === 'win32' // npm is npm.cmd on Windows
  });

  const shutdown = () => {
    if (!process.env.KEEP_HARNESS && !hasFlag('keep')) {
      rmSync(HARNESS, { force: true });
      rmSync(BOOT, { force: true });
    }
    try {
      child.kill('SIGTERM');
    } catch {
      /* already gone */
    }
  };
  process.on('SIGINT', () => {
    shutdown();
    process.exit(0);
  });
  process.on('SIGTERM', shutdown);
  child.on('exit', (code) => {
    shutdown();
    process.exit(code ?? 0);
  });

  for (let attempt = 0; attempt < 90; attempt++) {
    await new Promise((resolve) => setTimeout(resolve, 2000));
    if (await reachable(HARNESS_URL)) {
      console.log('');
      console.log('  ┌───────────────────────────────────────────────────────────');
      console.log('  │  Warehouse prêt (données de démo, sans WinCC OA) :');
      console.log(`  │  ${HARNESS_URL}`);
      console.log('  │  Bandeau "hors ligne" = le mode démo est actif.');
      console.log('  │  Ctrl+C pour arrêter.');
      console.log('  └───────────────────────────────────────────────────────────');
      console.log('');
      if (OPEN) openBrowser(HARNESS_URL);
      return; // keep the dev server in the foreground
    }
  }
  console.error('✗ le serveur de dev n\'est pas monté sur ' + DEV_URL);
  shutdown();
  process.exit(1);
}

await main();
