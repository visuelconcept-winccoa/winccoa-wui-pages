// -----------------------------------------------------------------------------
// Screenshot the Warehouse page WITHOUT a WinCC OA backend.
//
//   node tools/screenshot-warehouse-demo.mjs [--out <dir>] [--width N] [--height N]
//
// Unlike tools/screenshot-pages.mjs (which needs a live OA + login), this drives
// the page's built-in offline fallback: every store transparently switches to the
// in-memory demo dataset when the PARA backend is unreachable. To exercise that
// path deterministically the dev server is started with BASE_URL pointing at a
// dead port, and the page is mounted through a minimal harness HTML (written into
// the scaffolded apps/dashboard-wc/, which is un-versioned) that loads the iX
// theme + DI services but no shell, router or login.
//
// Captured (default docs/images/manual/):
//   warehouse.png                  Overview — one card per warehouse
//   warehouse-plan.png             Plan tab (2D), one location selected
//   warehouse-plan-edit.png        Plan tab (2D) in layout-edit mode (handles)
//   warehouse-plan3d.png           Plan tab (3D three.js scene)
//   warehouse-stock.png            Stock tab (KPIs + table)
//   warehouse-zones.png            Zones tab
//   warehouse-products.png         Products tab
//   warehouse-inventory.png        Inventory tab, campaign list
//   warehouse-inventory-sheet.png  Count sheet with entered counts + variance
//   warehouse-dialog.png           "Add stock" entity dialog
// -----------------------------------------------------------------------------
import { spawn } from 'node:child_process';
import { existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { chromium } from '@playwright/test';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const argumentValue = (name, fallback) => {
  const index = process.argv.indexOf(`--${name}`);
  return index !== -1 && process.argv[index + 1] ? process.argv[index + 1] : fallback;
};

const DEV_URL = 'http://127.0.0.1:4300';
const OUT = path.resolve(repoRoot, argumentValue('out', 'docs/images/manual'));
const WIDTH = Number(argumentValue('width', '1600'));
const HEIGHT = Number(argumentValue('height', '1000'));
// A dead proxy target: every /api|/WebUI_* call fails fast → offline demo mode.
const DEAD_BASE_URL = 'https://127.0.0.1:59999';
const HARNESS = path.join(repoRoot, 'apps/dashboard-wc/warehouse-demo.html');
const SETTLE = 1200;

const HARNESS_HTML = `<!doctype html>
<!-- Generated by tools/screenshot-warehouse-demo.mjs — safe to delete. -->
<html lang="en" data-ix-theme="classic" data-ix-color-schema="dark">
  <head>
    <meta charset="utf-8" />
    <title>wui-warehouse demo (no WinCC OA)</title>
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <meta name="ix:legacy-icons" content="false" />
    <link rel="stylesheet" href="/src/styles.scss" />
    <style>
      html, body { height: 100%; margin: 0; }
      wui-warehouse { display: block; height: 100%; }
    </style>
  </head>
  <body>
    <wui-warehouse></wui-warehouse>
    <script type="module">
      import 'reflect-metadata'; // tsyringe — must precede the services
      import '@wincc-oa/wui-ix-wrappers/wui-ix-loader/wui-ix-loader.js'; // iX theme + icons
      import '@wincc-oa/wui-base/wui-register-services.js'; // DI registrations
      // Load the page like the shell router does: a runtime dynamic import of the
      // '?import' URL, which the dev-server bypass serves from the TypeScript
      // source (see createProxyConfig in vite.shared.ts). Static imports won't do:
      // vite's import-analysis fails on the virtual /data/... path.
      await import(/* @vite-ignore */ '/data/dashboard-wc/pages/warehouse.js' + '?import');
      // Minimal router shim: the page navigates master/detail by dispatching
      // RouterEvent ('navigateTo'); mirror the /warehouse/:id path into the
      // component's warehouseid attribute like the shell router would.
      globalThis.addEventListener('navigateTo', (e) => {
        const path = e.detail?.pathname ?? '';
        const match = new RegExp('^/warehouse/([^/]+)$').exec(path);
        const el = document.querySelector('wui-warehouse');
        if (match) el.setAttribute('warehouseid', decodeURIComponent(match[1]));
        else el.removeAttribute('warehouseid');
      });
    </script>
  </body>
</html>
`;

async function reachable(url) {
  try {
    const response = await fetch(url, { signal: AbortSignal.timeout(2000) });
    return response.ok;
  } catch {
    return false;
  }
}

async function startDevelopmentServer() {
  if (await reachable(`${DEV_URL}/warehouse-demo.html`)) return; // reuse the running server
  console.log(`· starting dev server (BASE_URL=${DEAD_BASE_URL} — dead on purpose)`);
  const child = spawn('npm', ['start'], {
    cwd: repoRoot,
    env: { ...process.env, BASE_URL: DEAD_BASE_URL },
    stdio: 'ignore',
    detached: true
  });
  for (let attempt = 0; attempt < 60; attempt++) {
    await new Promise((resolve) => setTimeout(resolve, 2000));
    if (await reachable(`${DEV_URL}/warehouse-demo.html`)) return child;
  }
  throw new Error('dev server did not come up on ' + DEV_URL);
}

function stopDevelopmentServer(child) {
  if (!child) return;
  try {
    process.kill(-child.pid, 'SIGTERM');
  } catch {
    /* already gone */
  }
}

async function shoot(page, name) {
  await page.waitForTimeout(SETTLE);
  const file = path.join(OUT, name);
  await page.screenshot({ path: file });
  console.log(`  ✓ ${path.relative(repoRoot, file)}`);
}

/**
 * Launch Chromium; when the Playwright-managed download is missing (pinned
 * revision not installed), fall back to CHROMIUM_PATH or a preinstalled
 * /opt/pw-browsers/chromium instead of downloading browsers.
 */
async function launchChromium() {
  try {
    return await chromium.launch({ headless: true });
  } catch (error) {
    const fallback = process.env.CHROMIUM_PATH || '/opt/pw-browsers/chromium';
    if (!existsSync(fallback)) throw error;
    console.log(`· using system chromium at ${fallback}`);
    return chromium.launch({ headless: true, executablePath: fallback });
  }
}

async function main() {
  mkdirSync(OUT, { recursive: true });
  writeFileSync(HARNESS, HARNESS_HTML);
  const server = await startDevelopmentServer();

  const browser = await launchChromium();
  try {
    const page = await browser.newPage({ viewport: { width: WIDTH, height: HEIGHT } });
    page.on('pageerror', (error) => console.log('  [pageerror]', String(error).split('\n')[0]));
    await page.goto(`${DEV_URL}/warehouse-demo.html`, { waitUntil: 'domcontentloaded' });

    const root = page.locator('wui-warehouse');
    // The offline banner proves the demo fallback engaged (i.e. no WinCC OA).
    await root.locator('.offline').waitFor({ timeout: 30_000 });
    const tab = (label) => root.locator('ix-tab-item', { hasText: label });

    // Overview — one card per warehouse.
    await root.locator('wh-overview .card').first().waitFor();
    await shoot(page, 'warehouse.png');

    // Open the first warehouse → Plan (2D), select a stocked location.
    await root.locator('wh-overview .card', { hasText: 'Entrepôt Nord' }).click();
    await root.locator('wh-plan').waitFor();
    await root.locator('svg g.loc', { hasText: 'B-01' }).first().click();
    await shoot(page, 'warehouse-plan.png');

    // Layout-edit mode (drag/resize handles).
    await root.locator('.plan-toolbar ix-button', { hasText: 'Edit layout' }).click();
    await shoot(page, 'warehouse-plan-edit.png');
    await root.locator('.plan-toolbar ix-button', { hasText: 'Done' }).click();

    // 3D scene.
    await root.locator('.plan-toolbar ix-button', { hasText: '3D' }).click();
    await root.locator('wh-plan3d canvas').waitFor();
    await page.waitForTimeout(1500); // let the WebGL scene settle
    await shoot(page, 'warehouse-plan3d.png');

    await tab('Stock').click();
    await shoot(page, 'warehouse-stock.png');

    await tab('Zones').click();
    await shoot(page, 'warehouse-zones.png');

    await tab('Products').click();
    await shoot(page, 'warehouse-products.png');

    // Stock dialog (generic entity dialog).
    await tab('Stock').click();
    await root.locator('wh-stock ix-button', { hasText: 'Add stock' }).click();
    await shoot(page, 'warehouse-dialog.png');
    await page.locator('wh-entity-dialog ix-button', { hasText: 'Cancel' }).click();

    // Inventory — create a campaign, enter a few counts, show the variance.
    await tab('Inventory').click();
    await root.locator('wh-inventory ix-button', { hasText: 'New campaign' }).click();
    const dialog = page.locator('wh-entity-dialog');
    // Real keystrokes: ix-input only emits valueChange for genuine input events.
    await dialog.locator('ix-input').first().locator('input').click();
    await page.keyboard.type('Comptage démo');
    await dialog.locator('ix-button', { hasText: 'Save' }).click();
    await root.locator('wh-inventory tbody tr').first().waitFor();
    await shoot(page, 'warehouse-inventory.png');

    await root.locator('wh-inventory ix-icon-button[icon="visible"]').first().click();
    const counts = root.locator('wh-inventory .count-input input');
    await counts.nth(0).fill('1480'); // system 1500 → −20
    await counts.nth(1).fill('120'); // system 120 → 0
    await counts.nth(2).fill('200'); // system 180 → +20
    await shoot(page, 'warehouse-inventory-sheet.png');
  } finally {
    await browser.close();
    stopDevelopmentServer(server);
    if (!process.env.KEEP_HARNESS) rmSync(HARNESS, { force: true });
  }
  console.log('Done.');
}

await main();
